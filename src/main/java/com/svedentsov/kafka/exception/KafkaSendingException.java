package com.svedentsov.kafka.exception;

/**
 * Специализированное непроверяемое исключение (runtime exception), используемое для инкапсуляции
 * всех видов ошибок, возникающих в процессе отправки сообщений в Kafka.
 * Использование этого исключения позволяет вызывающему коду обрабатывать один конкретный тип ошибки,
 * не загромождая логику множеством {@code catch}-блоков для технических исключений от клиента Kafka.
 * Исходное исключение (причина) всегда сохраняется в поле {@code cause} для дальнейшего анализа.
 */
public class KafkaSendingException extends RuntimeException {
    /**
     * Конструктор для создания исключения с сообщением и исходной причиной.
     *
     * @param message Детальное сообщение, описывающее контекст ошибки.
     * @param cause   Исходное исключение (например, от Kafka-клиента), которое привело к ошибке.
     */
    public KafkaSendingException(String message, Throwable cause) {
        super(message, cause);
    }
}
